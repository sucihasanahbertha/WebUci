<h1 class="text-center text primary mt-3">Reply 1988</h1>
<img src="/img/reply.jpg" width="500px">
  <p>Drama dengan berlatar belakang tahun 1988 ini menceritakan tentang persahabatan yang terdiri dari empat orang laki-laki dan satu perempuan. 
Mereka adalah Deok Sun (Lee Hye-ri), Sun Woo (Go Kyung-pyo), Kim Jung Hwan (Ryun Jun-yeol), Dong Ryong (Lee Dong-hwi), dan Cho Taek (Park Bo-gum).</p>
<p>Deok Sun merupakan anak yang selalu ceria dan gemar menyanyi serta menari. Ia menjadi perempuan satu-satunya dalam persahabatan ini.  
Deok Sun hidup dalam keluarga yang miskin. Sang ayah, Dong Il (Sung Dong-Il), kehilangan uangnya setelah menjamin hutang orang lain.  
Meski dikenal cerita, Deok Sun kerap bertengkar dengan kakak perempuannya, Boo-ra (Ryoo Hye-young) di rumah.</p>
<p>Lalu, Sun Woo merupakan anak yang penurut dan baik hati. Ia juga memiliki jiwa peduli yang sangat tinggi serta perhatian pada orang lain.  
Sun Woo juga dikenal sebagai murid teladan di sekolah. Dia dan adiknya dibesarkan oleh sang ibu seorang diri setelah ayahnya meninggal dunia. 
Sementara itu, Kim Jung Hwan memiliki sikap yang cuek dan cool, namun ia diam-diam sangat peduli pada orang lain di sekelilingnya.</p>
<p>Kim Jung Hwan berasal dari keluarga yang awalnya sangat miskin, kemudian sang kakak memenangkan lotre yang mengubah hidup mereka menjadi kaya raya. 
Selain itu, ada Dong Ryong, anak yang paling gaul, cerewet dan sering membuat ulah.  
Setelah menyelesaikan pendidikannya, Dong Ryong memutuskan untuk tidak menlajutkan ke tingkat universitas karena memiliki nilai akademik yang buruk.</p>
